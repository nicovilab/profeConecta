    public void updateCalendarHighlighting(Set<LocalDate> noBookingDates, Set<LocalDate> availableBookingDates, Set<LocalDate> fullyBookedDates) {
        DatePickerSettings settings = new DatePickerSettings();
        settings.setHighlightPolicy(new BookingHighlightPolicy(noBookingDates, availableBookingDates, fullyBookedDates));
        getCalendarPanel().setSettings(settings);
    }

